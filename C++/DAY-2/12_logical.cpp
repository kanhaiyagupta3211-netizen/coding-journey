#include <iostream>
using namespace std;

int main()
{
    cout << "true && true  = " << (true && true) << "\n";
    cout << "true && false = " << (true && false) << "\n";

    cout << "true || false = " << (true || false) << "\n";
    cout << "false || false = " << (false || false) << "\n";

    cout << "!true  = " << (!true) << "\n";
    cout << "!false = " << (!false) << "\n";

    return 0;
}